    private int obtenerIDprovincia(String provinciaNombre) {
        int provinciaID = -1;

        String sql = "SELECT idProvincias FROM Provincias WHERE provNombre = ?";
        
        try {
            PreparedStatement preparedStatement = conectar.prepareStatement(sql);
            preparedStatement.setString(1, provinciaNombre);
            
            ResultSet resultSet = preparedStatement.executeQuery();
            
            if (resultSet.next()) {
                provinciaID = resultSet.getInt("idProvincias");
            }

            resultSet.close();
            preparedStatement.close();
        } catch (SQLException e) {
            Logger.getLogger(friLocalidad.class.getName()).log(Level.SEVERE, null, e);
        }
        
        return provinciaID;
    }
    
