    public void Guardar(){
        String nombre = txtNombre.getText();
        String codigopostal = txtCodigoPostal.getText();
        String provinciaSeleccionada = (String) cboProvincia.getSelectedItem();
        int provinciaid = obtenerIDprovincia(provinciaSeleccionada);
        String Sql = "INSERT INTO Localidad(locNombre, locCP, ProvinciaID) VALUES (?, ?, ?)";
        
        try{
            Statement set = conectar.createStatement();
            PreparedStatement pasar = conectar.prepareStatement(Sql);
            pasar.setString(1, nombre);
            pasar.setString(2, codigopostal);
            pasar.setInt(3, provinciaid);
            pasar.executeUpdate();
            JOptionPane.showMessageDialog(null, "Registro Exitoso");
        }catch(SQLException ex){
            Logger.getLogger(friLocalidad.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    

